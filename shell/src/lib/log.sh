# ==============================================================================
# log:: 日志输出
# ==============================================================================

log::info() { echo -e "${GREEN}[INFO]${PLAIN} $1"; }
log::warn() { echo -e "${YELLOW}[WARN]${PLAIN} $1"; }
log::err()  { echo -e "${RED}[ERROR]${PLAIN} $1"; }
log::step() { echo -e "${BLUE}[*]${PLAIN} $1"; }
